/// Defines day of week
enum WeekDays {
  /// Monday: 0
  monday,

  /// Tuesday: 1
  tuesday,

  /// Wednesday: 2
  wednesday,

  /// Thursday: 3
  thursday,

  /// Friday: 4
  friday,

  /// Saturday: 5
  saturday,

  /// Sunday: 6
  sunday,
}

